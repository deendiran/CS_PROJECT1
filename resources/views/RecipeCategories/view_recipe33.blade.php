                        <div class="instruction-list">
                            <h3>Instructions</h3>
                            <ul>
                                <li>
                              <span>01.</span>
                              In a small pot or skillet, place the oil and garlic and heat over medium-low until the 
                              garlic is just barely starting to turn golden brown and smells fragrant, about 5 minutes.
                                </li>
                                <li>
                                    <span>02.</span>
                                    In a large bowl or resealable container, place the drained beans, pour the hot garlic oil over and toss to combine.
                                     Add the lemon zest and oregano and season to taste with salt. Let sit at least 1 hour at room temperature,
                                      or seal and transfer to the fridge overnight. About 1 hour before serving, remove from the fridge to bring 
                                      up to room temperature so that the oil isn’t cold and congealed.

                                <li>
                                    <span>03.</span>
                                    At 30 minutes or 1 hour before serving, halve the cherry tomatoes (or cut the heirlooms into big bite-size pieces or wedges)
                                     and season liberally with salt. This helps intensify the tomato flavor—please don’t skip this step. 
                                     Let them sit in a separate bowl from the beans until just before ready to serve,
                                      then lift the tomatoes out (leaving the juices behind) and toss them into the beans.
                                       Taste and adjust seasoning as needed. If you want it to taste brighter, add a splash of sherry or red wine vinegar,
                                        and maybe add a few fresh oregano leaves.
                                     If you want a bit of spice, sprinkle in some crushed red pepper flakes. Transfer to a serving platter.</li>

